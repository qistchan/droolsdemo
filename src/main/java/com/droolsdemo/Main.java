    private void test() throws FileNotFoundException {
        Logger logger = LoggerFactory.getLogger(Object.class);
        logger.info(getTime() + "Startup.");
        //编译DRL文件
        complieDRL();

        KnowledgeBuilder kbuilder = KnowledgeBuilderFactory.newKnowledgeBuilder();

        //加载本地DRL文件
        logger.info(getTime() + "Loading RULE file.");
        String pathToDRL = System.getProperty("user.dir")+"\\testbom.drl";
        FileInputStream  fis = new FileInputStream(pathToDRL);
        kbuilder.add(ResourceFactory.newInputStreamResource(fis), ResourceType.DRL);

        //加载远程DRL文件
        //kbuilder.add(ResourceFactory.newUrlResource("http://localhost:8080/testbom.drl"), ResourceType.DRL);


        //创建知识库
        InternalKnowledgeBase kbase = KnowledgeBaseFactory.newKnowledgeBase();
        //将规则包加载到知识库中
        kbase.addPackages(kbuilder.getKnowledgePackages());

        // 初始化KieSession
        KieSession kSession = kbase.newKieSession();
        logger.info(getTime() + "Load RULE file finished.");


        // 添加监听器
        TrackingAgendaEventListener agendaEventListener = new TrackingAgendaEventListener();
        kSession.addEventListener(agendaEventListener);


        logger.info(getTime() + "Start resolution.");
        // 使用测试的SVR数据
        List<ResolveBOM> resolveBOMs = prepareSampleSVR();

        for(ResolveBOM resolveBOM:resolveBOMs){
            agendaEventListener.reset(); // 重置监听器
            //logger.info(getTime() + "Resolution processing. [" + resolveBOM.getSvr().getSvrID() + "]");
            kSession.insert(resolveBOM); // 插入Facts
            kSession.fireAllRules(); // 解析SVR
            //logger.info(getTime() + "Resolute done. [" + resolveBOM.getSvr().getSvrID() + "]");

            //打印解析内容
            System.out.println("\nThe reuslt of SVR: " + resolveBOM.getSvr().getSvrID() + "\n");
            for(Match match:agendaEventListener.getMatchList()){
                System.out.println(match.getRule().getName());
            }
        }

        logger.info(getTime() + "Finish resolution.");

        // 释放KieSession
        kSession.dispose();

    }


    private SVR formatSVR(String svrID, String svrVer, String svrString){
        Map<String, String>optionList = new HashMap<>();
        String[] optionKVArr = svrString.toUpperCase(Locale.ROOT).replaceAll("\"","").split(" AND ");
        for (String optionKV:optionKVArr) {
            String key = optionKV.split("=")[0].trim();
            String value = optionKV.split("=")[1].trim();
            optionList.put(key, value);
        }
        return new SVR(svrID, svrVer, optionList);
    }

    private void complieDRL(){
        Logger logger = LoggerFactory.getLogger(Object.class);
        logger.info(getTime() + "Start DRL compile.");

        //使用DRT模板编译DRL文本
        List<ECADBOM> ecadboms = prepareSampleBOMData();

        //获取资源文件夹中的DRT模板
        File file = new File(Objects.requireNonNull(Main.class.getResource("/rules/ecadbom.drt")).getPath());

        try (InputStream stream = new FileInputStream(file)){

            //准备测试数据
            String[][] ecdbomArray = new String[1000][2];
            for(int i=0;i<ecdbomArray.length;i++){
                ecdbomArray[i][0]=ecadboms.get(i).getBomUID();
                ecdbomArray[i][1]=ecadboms.get(i).getFormatUsage();
            }
            DataProvider dataProvider = new ArrayDataProvider(ecdbomArray);
            DataProviderCompiler dataProviderCompiler = new DataProviderCompiler();
            logger.info(getTime() + "Compiling.");

            //基于DRT编译DRL
            String drl = dataProviderCompiler.compile(dataProvider, stream);
            logger.info(getTime() + "Compile done.");

            //输出DRL到文件
            String pathToDRL = System.getProperty("user.dir")+"\\testbom.drl";
            FileOutputStream fos = new FileOutputStream(pathToDRL);
            logger.info(getTime() + "DRT file write successfully. The path is " + pathToDRL);
            fos.write(drl.getBytes());
            fos.close();

        } catch (IOException e){
            e.printStackTrace();
        }


    }

